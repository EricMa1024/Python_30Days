# 學習重點：在不同的型別做資料類型的轉換
# 例如：int 跟 float互相轉換

# 一、int 轉換成 float 有小數點
float(1)
print(float(1))

# 二、int(1.0)
int(1.0)
print(int(1.0))

# 三、int(1.6)
int(1.6)
print(int(1.6))

# 四、int(1.4)
int(1.4)
print(int(1.4))

# 五、int(-1.1)
int(-1.1)
print(int(-1.1))

# 六、int(-2.1)
int(-2.1)
print(int(-2.1))

# 七、浮點數+整數
print(1.5+1)

# 八、浮點數+整數
print(1+3.0)

